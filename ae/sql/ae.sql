   `ca`.`cupo_actual` AS `cupo_actual`,
   `ca`.`activo` AS `activo`,group_concat(concat(`ah`.`dia_semana`,' de ',time_format(`ah`.`hora_inicio`,'%H:%i'),' a ',time_format(`ah`.`hora_fin`,'%H:%i')) order by field(`ah`.`dia_semana`,'Lunes','Martes','Miércoles','Jueves','Viernes') ASC separator ' y ') AS `horarios`
